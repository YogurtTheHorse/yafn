		rsh2	= 0x3d,

		bxor	= 0x3f,

		/* ---------------- */

		cmp		= 0x40,
		eq		= 0x41,
		
		jmp		= 0x44,
		jez		= 0x45,
		jnz		= 0x46,
		
		call	= 0x47,
		ret		= 0x48,

		cmp0	= 0x49
	}
}
