namespace yafn.Instructions {
	public enum Instruction {
		emp		= 0x0,
		hlt		= 0x1,
		brp		= 0x2,
		wrt		= 0x3,
		srt		= 0x4,

		/* ---------------- */

		epush	= 0x11,
		ipush	= 0x12,
		mpush	= 0x13,
		cpush	= 0x14,
		
		pop		= 0x15,
		dpl		= 0x16,
		sav		= 0x17,
		swp		= 0x18,
		svs		= 0x19,

		rnd		= 0x1a,

		smpsh	= 0x1b,
		scpsh	= 0x1c,

		/* ---------------- */

		add		= 0x20,
		sub		= 0x21,
		mul		= 0x22,
		div		= 0x23,
		
		neg		= 0x24,
